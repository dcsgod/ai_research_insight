# ---------------------------------------------------------------------------


class ForecastResult(Base):
    """
    A statistical trend forecast for one entity at a specific future date.

    Stores the point prediction and confidence interval produced by the
    forecasting model (e.g. Prophet, ARIMA, or a custom neural model).

    One row per (entity_id, forecast_date, model_used).

    Attributes:
        id:               UUID primary key.
        entity_id:        UUID of the entity being forecast.
        entity_type:      Discriminator (paper | repository | topic).
        forecast_date:    The future date this prediction is for.
        predicted_value:  Point estimate of the trend score on forecast_date.
        confidence_lower: Lower bound of the 95% confidence interval.
        confidence_upper: Upper bound of the 95% confidence interval.
        model_used:       Model identifier (e.g. "prophet", "arima", "lstm").
        horizon_days:     Number of days ahead from computation date.
        created_at:       When the forecast was computed.
    """

    __tablename__ = "forecast_results"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
    )
    entity_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        nullable=False,
        index=True,
        comment="UUID of the entity being forecast",
    )
    entity_type: Mapped[EntityType] = mapped_column(
        Enum(EntityType, name="entity_type_enum", create_constraint=False),
        nullable=False,
        index=True,
    )
    forecast_date: Mapped[date] = mapped_column(
        Date,
        nullable=False,
        index=True,
        comment="Future calendar date this prediction is for",
    )
    predicted_value: Mapped[float] = mapped_column(
        Float,
        nullable=False,
        comment="Point estimate of the trend score",
    )
    confidence_lower: Mapped[Optional[float]] = mapped_column(
        Float,
        nullable=True,
        comment="Lower 95% confidence interval bound",
    )
    confidence_upper: Mapped[Optional[float]] = mapped_column(
        Float,
        nullable=True,
        comment="Upper 95% confidence interval bound",
    )
    model_used: Mapped[str] = mapped_column(
        String(64),
        nullable=False,
        default="prophet",
        comment="Forecasting model identifier",
    )
    horizon_days: Mapped[Optional[int]] = mapped_column(
        Integer,
        nullable=True,
        comment="Days ahead from the computation date",
    )
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=lambda: datetime.now(timezone.utc),
        server_default=func.now(),
        index=True,
    )

    __table_args__ = (
        Index("ix_forecast_entity_date", "entity_id", "forecast_date"),
        Index("ix_forecast_entity_type_date", "entity_type", "forecast_date"),
        Index("ix_forecast_predicted_desc", predicted_value.desc()),
        {"comment": "Statistical trend forecasts per entity and future date"},
    )

    def __repr__(self) -> str:
        return (
            f"<ForecastResult entity_id={self.entity_id!s:.8s} "
            f"date={self.forecast_date} value={self.predicted_value:.4f}>"
        )

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": str(self.id),
            "entity_id": str(self.entity_id),
            "entity_type": self.entity_type.value,
            "forecast_date": self.forecast_date.isoformat(),
            "predicted_value": self.predicted_value,
            "confidence_lower": self.confidence_lower,
            "confidence_upper": self.confidence_upper,
            "model_used": self.model_used,
            "horizon_days": self.horizon_days,
            "created_at": self.created_at.isoformat(),
        }
