"""
SQLAlchemy ORM models for LLM-generated insights and forecast results.

Tables:
  - insights         : LLM-generated textual summaries per entity
  - forecast_results : Statistical trend forecasts per entity
"""

from __future__ import annotations

import uuid
from datetime import date, datetime, timezone
from typing import Any, Dict, Optional

from sqlalchemy import (
    Date,
    DateTime,
    Enum,
    Float,
    Index,
    Integer,
    String,
    Text,
    func,
)
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy.orm import Mapped, mapped_column

from backend.db.database import Base
from backend.models.trend_signal import EntityType


# ---------------------------------------------------------------------------
# Insight
# ---------------------------------------------------------------------------


class Insight(Base):
    """
    An LLM-generated textual insight for a research entity.

    One row per (entity_id, llm_model, created_at) — multiple insight
    versions can be stored to track quality over time or model upgrades.

    Attributes:
        id:            UUID primary key.
        entity_id:     UUID of the related Paper, Repository, or Topic row.
        entity_type:   Discriminator (paper | repository | topic).
        insight_text:  Full natural-language insight generated by the LLM.
        insight_type:  Category of insight (summary | analysis | comparison | forecast_narrative).
        llm_model:     Model identifier used for generation (e.g. "gpt-4o-mini").
        prompt_tokens: Number of prompt tokens consumed.
        completion_tokens: Number of completion tokens generated.
        tokens_used:   Total token count (prompt + completion).
        created_at:    Generation timestamp (UTC).
    """

    __tablename__ = "insights"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
    )
    entity_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        nullable=False,
        index=True,
        comment="UUID of the related entity",
    )
    entity_type: Mapped[EntityType] = mapped_column(
        Enum(EntityType, name="entity_type_enum", create_constraint=False),
        nullable=False,
        index=True,
    )
    insight_text: Mapped[str] = mapped_column(
        Text,
        nullable=False,
        comment="LLM-generated insight text",
    )
    insight_type: Mapped[str] = mapped_column(
        String(64),
        nullable=False,
        default="summary",
        comment="summary | analysis | comparison | forecast_narrative | daily_digest",
    )
    llm_model: Mapped[str] = mapped_column(
        String(128),
        nullable=False,
        comment="LLM model identifier used for generation",
    )
    prompt_tokens: Mapped[Optional[int]] = mapped_column(
        Integer,
        nullable=True,
    )
    completion_tokens: Mapped[Optional[int]] = mapped_column(
        Integer,
        nullable=True,
    )
    tokens_used: Mapped[Optional[int]] = mapped_column(
        Integer,
        nullable=True,
        comment="Total tokens consumed (prompt + completion)",
    )
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=lambda: datetime.now(timezone.utc),
        server_default=func.now(),
        index=True,
    )

    __table_args__ = (
        Index("ix_insights_entity_created", "entity_id", "entity_type", "created_at"),
        Index("ix_insights_type_created", "insight_type", "created_at"),
        {"comment": "LLM-generated insights for papers, repositories, and topics"},
    )

    def __repr__(self) -> str:
        return (
            f"<Insight entity_id={self.entity_id!s:.8s} "
            f"model={self.llm_model} created={self.created_at}>"
        )

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": str(self.id),
            "entity_id": str(self.entity_id),
            "entity_type": self.entity_type.value,
            "insight_text": self.insight_text,
            "insight_type": self.insight_type,
            "llm_model": self.llm_model,
            "prompt_tokens": self.prompt_tokens,
            "completion_tokens": self.completion_tokens,
            "tokens_used": self.tokens_used,
            "created_at": self.created_at.isoformat(),
        }


